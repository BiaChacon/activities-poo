
public class Ambiente {
	private int qtsLinhas, qtsColunas;

	public Ambiente(int linhas, int colunas){
		this.qtsLinhas = linhas;
		this.qtsColunas = colunas;
	}
	
	public int getQtsLinhas() {
		return qtsLinhas;
	}

	public void setQtsLinhas(int qtsLinhas) {
		this.qtsLinhas = qtsLinhas;
	}

	public int getQtsColunas() {
		return qtsColunas;
	}

	public void setQtsColunas(int qtsColuna) {
		this.qtsColunas = qtsColuna;
	}
	

}
